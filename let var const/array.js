var arr=[14,27,28,57]

//pop method delete element in last

arr.pop()

console.log('pop array' , arr )

//push method add element in last

arr.push(101)

console.log('push array' , arr)

//shift method  delete element in array

arr.shift()

console.log('shift method' , arr)

//unshift method add element first in array

arr.unshift(102)

console.log('unshift method' , arr)